package persistence.entities;

import javax.persistence.*;

@Entity
@Table(name = "T11_CUSTOMER")
public class Customer {

    @Id
    @Column(name = "C11_CUSTOMER_ID")
    private Integer id;

    @Column(name = "C11_CUSTOMER_NAME")
    private String name;

    @Column(name = "C11_CUSTOMER_EMAIL")
    private String email;

    @Column(name = "C11_CUSTOMER_ADDRESS")
    private String address;

    @Column(name = "C11_CUSTOMER_PHONE")
    private String phone;

    @Column(name = "C11_CUSTOMER_USERNAME")
    private String username;

    @Column(name = "C11_CUSTOMER_PASSWORD")
    private String password;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }


    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Customer customer = (Customer) o;
        return id.equals(customer.id);
    }

    @Override
    public int hashCode() {
        return id.hashCode();
    }


    @Override
    public String toString() {
        return "Customer{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", email='" + email + '\'' +
                ", address='" + address + '\'' +
                ", phone='" + phone + '\'' +
                ", username='" + username + '\'' +
                ", password='" + password + '\'' +
                '}';
    }
}
