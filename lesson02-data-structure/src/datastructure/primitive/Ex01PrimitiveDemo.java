package datastructure.primitive;

public class Ex01PrimitiveDemo {
	public static void main(String[] args) {
		int a = 5;
		char b = 'x';
		float c = 5;
		float d = 4.5f;
		boolean isValid = true;
		
		System.out.println("giá trị số nguyên " + a);
		System.out.println("giá trị ký tự " + b);
		System.out.println("giá trị số thực c " + c);
		System.out.println("giá trị số thực d " + d);
		System.out.println("giá trị kiểu boolean " + isValid);
	}
}
