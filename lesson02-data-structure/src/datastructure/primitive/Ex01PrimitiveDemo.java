package datastructure.primitive;

public class Ex01PrimitiveDemo {
	
	public static void main(String[] args) {
		
		int a = 5;
		char b = 'd';
		float c = 295;
		float d = 29.5f;
		boolean e = true;
		
		System.out.println("Giá trị số nguyên a --> " + a);
		System.out.println("Giá trị kí tự --> " + b);
		System.out.println("Giá trị số thực c --> " + c);
		System.out.println("Giá trị số thực d --> " + d);
		System.out.println("Giá trị kiểu boolean --> " + e);

	}
}
