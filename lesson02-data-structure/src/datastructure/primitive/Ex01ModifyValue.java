package datastructure.primitive;


public class Ex01ModifyValue {
	

	public static void main(String[] args) {
		
		int a = 5;
		char b = '@';
		float c = 5;
		float d = 5.2f;
		boolean isValid = true;
		

		System.out.println("gia tri so nguyen --> " + a);
		System.out.println("gia tri so ki tu --> " + b);
		System.out.println("gia tri so thuc c --> " + c);
		System.out.println("gia tri so thuc d --> " + d);
		System.out.println("gia tri kieu boolean --> " + isValid);

		
		

	}

}
